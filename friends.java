package bigwork;

public class friends {
	String from;
	String copyer;
	String T;
	public friends(String from, String copyer, String t) {
		super();
		this.from = from;
		this.copyer = copyer;
		T = t;
	}
	public void set(String from, String copyer, String t) {
		this.from = from;
		this.copyer = copyer;
		T = t;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getCopyer() {
		return copyer;
	}
	public void setCopyer(String copyer) {
		this.copyer = copyer;
	}
	public String getT() {
		return T;
	}
	public void setT(String t) {
		T = t;
	}
	

}
